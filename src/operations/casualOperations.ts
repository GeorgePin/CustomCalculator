export function addition(firstNumber: number, secondNumber: number) {
  return (firstNumber + secondNumber).toString();
}
export function subtraction(firstNumber: number, secondNumber: number) {
  return (firstNumber - secondNumber).toString();
}

export function division(firstNumber: number, secondNumber: number) {
  return (firstNumber / secondNumber).toString();
}
export function product(firstNumber: number, secondNumber: number) {
  return (firstNumber * secondNumber).toString();
}
export function percentage(firstNumber: number) {
  return (firstNumber / 100).toString();
}
export function signChanging(firstNumber: number) {
  return (-firstNumber).toString();
}
export function calculateOneDivideByValue(firstNumber: number) {
  return (1 / firstNumber).toString();
}
